    '— UV: високий — крем і головний убір; помірний — можна порекомендувати; низький/хмарно — не навʼязувати.',
    '— Вітер: сильний — вітрозахис; холод + вітер — відчуття холоду вище, врахуй у пораді.',
    '— Профіль: біг/велосипед — легше через рух, велосипед ще видимість; з дитиною — захист і запас; офіс — зручно в приміщенні.',
